    private void go1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_go1ActionPerformed
        String sym = (String) s1cb.getSelectedItem();
        if(!sym.equals(p2sym)){
            p1sym = sym;
            String name = name1txt.getText();
            if(!name.equals(""))
                p1name=name;
            if(!humancb1.isSelected()){
                p1name = "Computer 2";
                c1play=true;
            }
            name1txt.setEnabled(false);
            s1cb.setEnabled(false);
            go1.setEnabled(false);
            humancb1.setEnabled(false);
            p1lbl.setText(p1name);
        }else{
            JOptionPane.showMessageDialog(null,"Player 1, please choose another symbol");
        }
        
    }//GEN-LAST:event_go1ActionPerformed

