    boolean checkwin(int pl){
        if(pl==1){
            if(wins(pl1)){
                JOptionPane.showMessageDialog(null,p1name+" Won the Game");
                over();
                p1++;
                round++;
                lastwinner=p1name;
                return true;
            }
        }else if(pl==2){
            if(wins(pl2)){
                JOptionPane.showMessageDialog(null,p2name+" Won the Game");
                over();
                p2++;
                round++;
                lastwinner=p2name;
                return true;
            }
        }
        refreshScore();
        return false;
    }
    void checkDraw(){
        if(Draw()){
            draw++;
            round++;
            JOptionPane.showMessageDialog(null,"The round ended in a Draw");
        }
        refreshScore();
    }
    boolean Draw(){
        int p1 = pl1.size();
        int p2 = pl2.size();
        if(!wins(pl1) && !wins(pl2) && (p1+p2)==9 ){
            return true;
        }
        return false;
    }
    void refreshScore(){
        roundtxt.setText(""+round);
        p1txt.setText(""+p1);
        p2txt.setText(""+p2);
        drawtxt.setText(""+draw);
        if(p1>p2){
            leadtxt.setText(p1name);
        }else if(p2>p1){
            leadtxt.setText(p2name);
        }
        lasttxt.setText(lastwinner);
    }
